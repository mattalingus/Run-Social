export async function seedCommunityPaths(): Promise<void> {
  await pool.query(
    `CREATE UNIQUE INDEX IF NOT EXISTS idx_community_paths_seed_name
     ON community_paths (name) WHERE created_by_name = 'PaceUp'`
  );

  let inserted = 0;

  for (const p of SEED_PATHS) {
    const startCoord = p.coordinates[0];
    const endCoord = p.coordinates[p.coordinates.length - 1];

    const result = await pool.query(
      `INSERT INTO community_paths
        (name, route_path, distance_miles, is_public, created_by_user_id, created_by_name, start_lat, start_lng, end_lat, end_lng, activity_type, run_count)
       VALUES ($1, $2, $3, true, NULL, 'PaceUp', $4, $5, $6, $7, 'run', $8)
       ON CONFLICT DO NOTHING`,
      [
        p.name,
        JSON.stringify(p.coordinates),
        p.distance_miles,
        startCoord.latitude,
        startCoord.longitude,
        endCoord.latitude,
        endCoord.longitude,
        p.run_count,
      ]
    );
    if (result.rowCount && result.rowCount > 0) inserted++;
  }

  if (inserted > 0) {
    console.log(`[seed-paths] Inserted ${inserted} community paths across US cities`);
  } else {
    console.log(`[seed-paths] All community paths already seeded, no duplicates inserted`);
  }
}
